def palette(mask):
    h, w = mask.shape
    board = np.zeros((h, w, 3))
    id_len = int(np.max(mask.ravel()))
    for id_ in range(1, id_len+1):
        board[mask == id_, :] = _COLORS[id_]
    
    return board * 255
  
  
